package com.myhome.zipcode;

public class ZipcodeDto {
	private String zipcode;
	private int seq;
	private String sido;
	private String sigungu;
	private String eupmun;
	private String li;
	private String dosu;
	private String bunzi;
	private String apt;
	private String address;
	private String wdate;

	public String getZipcode() {
		return zipcode;
	}

	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}

	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}

	public String getSido() {
		return sido;
	}

	public void setSido(String sido) {
		this.sido = sido;
	}

	public String getSigungu() {
		return sigungu;
	}

	public void setSigungu(String sigungu) {
		this.sigungu = sigungu;
	}

	public String getEupmun() {
		return eupmun;
	}

	public void setEupmun(String eupmun) {
		this.eupmun = eupmun;
	}

	public String getLi() {
		return li;
	}

	public void setLi(String li) {
		this.li = li;
	}

	public String getDosu() {
		return dosu;
	}

	public void setDosu(String dosu) {
		this.dosu = dosu;
	}

	public String getBunzi() {
		return bunzi;
	}

	public void setBunzi(String bunzi) {
		this.bunzi = bunzi;
	}

	public String getApt() {
		return apt;
	}

	public void setApt(String apt) {
		this.apt = apt;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getWdate() {
		return wdate;
	}

	public void setWdate(String wdate) {
		this.wdate = wdate;
	}

}
